package Exercicios.Basico;

public class MediaSalarial {

	public static void main(String[] args) {
		double fun1, fun2, fun3, fun4;
		
		fun1 = 5200.00;
		fun2 = 4350.00;
		fun3 = 4800.00;
		fun4 = 3600.00;
		
		double media = (fun1 + fun2 + fun3 + fun4) / 4;
		System.out.println("A média salarial da empresa é de R$ " + media);
	}

}
